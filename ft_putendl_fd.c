#include "libft.h"

void	ft_putendl_fd(const char *str, int fd)
{
	size_t		i;
	char		*s;

	i = 0;
	s = (char *)str;
	while (i < ft_strlen(s))
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
