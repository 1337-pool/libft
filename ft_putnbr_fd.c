#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long	n;
	char	res;

	n = nb;
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	res = n % 10 + '0';
	write(fd, &res, 1);
}
